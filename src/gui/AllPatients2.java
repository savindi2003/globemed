    private void jTextField7KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField7KeyReleased
        String search = jTextField7.getText();

        try {

            ResultSet rs = MySQL.execute("SELECT * FROM `patient` "
                    + "INNER JOIN `branch` ON `patient`.`branch_id` = `branch`.`id` WHERE `patient`.`name` LIKE '%" + search + "%'");

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);

            int counter = 1;
            while (rs.next()) {

                Vector<String> v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("age"));
                v.add(rs.getString("gender"));
                v.add(rs.getString("mobile"));
                v.add(rs.getString("dob"));
                v.add(rs.getString("address"));
                v.add(rs.getString("registerd_date"));
                v.add(rs.getString("branch.name"));
                v.add("IN0" + rs.getString("insurance_id"));

                model.addRow(v);
                jTable2.setModel(model);
                counter++;

            }

        } catch (Exception e) {
            e.printStackTrace();
            log1.warning(e.toString());
            JOptionPane.showMessageDialog(this, "Something Went Wrong..", "Error", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_jTextField7KeyReleased

